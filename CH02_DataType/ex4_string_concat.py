# 문자열 연산
# 문자열을 더하거나 곱하기

# 문자열 덧셈
head = "Python"
tail = " is fun!"
print(head + tail) # + 연산자를 통한 문자열 덧셈셈

# 문자열 곱셈
a = "Python"
print(a * 2) # * 연산자를 사용하여 문자열을 2번 반복(곱함)

# 문자열 곱셈 응용
print("=" * 50)
print("My Program")
print("=" * 50) # = 을 50번 곱셈하여 콘솔에 보기좋게 출력할 수 있다.

# 문자열 길이 구하기
b = "Life is too short"
print(len(b)) # len(문자열) 함수를 통해 문자열의 길이를 구할 수 있다.