# 정수형
a = 123
a = -178
a = 0

# 실수형
a = 1.2
a = -3.45
a = 4.24E10 # 4.24 * 10의 10제곱
a = 4.24e-10 # 4.24 * 10의 -10제곱

# 8진수, 16진수
a = 0o177 # 0o로 시작하면 8진수
a = 0x8ff # 0x로 시작하면 16진수

# 사칙연산
a = 3
b = 4
print(a + b) # 7 출력
print(a - b) # -1 출력
print(a * b) # 12 출력
print(a / b) # 0.75 출력

# 제곱을 나타내는 ** 연산자
a = 3
b = 4
print(a ** b) # 3의 4 제곱인 81 출력

# 나머지 연산자 %
print(7 % 3) # 1 출력
print(3 % 7) # 3 출력

# 몫을 반환하는 연산자 //
print(7 // 4) # 1 출력 (몫에 해당하는 정수값은 1)

