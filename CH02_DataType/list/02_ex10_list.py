# 리스트(List) 자료형
# 데이터를 모아서 관리하는 자료형

# 리스트 생성
odd = [1, 3, 5, 7, 9] # 리스트명 = [요소1, 요소2, 요소3, ...] 으로 생성한다.

# 여러 가지 리스트
a = [] # 비어있는 리스트 (a = list()로 생성할 수 있음.)
b = [1, 2, 3] # 숫자를 요소로 갖는 리스트
c = ["Life", "is", "too", "short"] # 문자열을 요소로 갖는 리스트
d = [1, 2, "Life", "is"] # 숫자와 문자열을 함께 요소로 갖는 리스트
d = [1, 2, ["Life", "is"]] # 리스트 자체를 요소로 갖는 리스트
# 즉 리스트 안에는 어떤 자료형도 포함시킬 수 있다.